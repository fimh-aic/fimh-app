class FoodEntity {
  final String name;
  final num calories;
  final num servingSize;
  final num fatTotal;
  final num fatSaturated;
  final num protein;
  final num sodium;
  final num potassium;
  final num cholesterol;
  final num carbohydrates;
  final num fiber;
  final num sugar;

  FoodEntity({
    required this.name,
    required this.calories,
    required this.servingSize,
    required this.fatTotal,
    required this.fatSaturated,
    required this.protein,
    required this.sodium,
    required this.potassium,
    required this.cholesterol,
    required this.carbohydrates,
    required this.fiber,
    required this.sugar,
  });
}
